//Arithmetic operators:
let a = 9;
let b= 10;

console.log("a = " , a , " & b = " , b);

console.log("a + b = " , a + b);
console.log("a - b = " , a - b);
console.log("a * b = " , a * b);
console.log("a / b = " , a / b);
console.log("a % b = " , a % b);
console.log("a ** b = " , a ** b);

//Unary Operator
let c = 8;
let d = 6;
c++;
console.log("c = " , c);
d--;
console.log("d = " , d);


//comparison operators
let e = 4;
